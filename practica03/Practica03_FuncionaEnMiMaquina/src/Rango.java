/**
 * Enumeración que representa los rangos jerárquicos de los ninjas.
 * Los rangos determinan el número de aspirantes que puede liderar un ninja.
 * 
 */
public enum Rango {
    /** Rango más bajo, puede liderar 1 aspirante */
    GENIN, 
    /** Rango intermedio, puede liderar 2 aspirantes */
    CHUNIN, 
    /** Rango más alto, puede liderar 3 aspirantes */
    JONIN
}
